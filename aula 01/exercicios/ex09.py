salarioFuncionario = float(input("Digite seu salário: "))
if salarioFuncionario > 12500:
    resultado1 = salarioFuncionario + (salarioFuncionario * 0.1)
    print(resultado1)
else:
    resultado2 = salarioFuncionario + (salarioFuncionario * 0.15)
    print(resultado2)

